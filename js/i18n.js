window.currentLang = localStorage.getItem('lang') || 'en';

window.t = function(key) {
  return window.translations[window.currentLang]?.[key]
    || window.translations.en[key]
    || key;
};

const LANG_META = {
  en: { flag: 'assets/images/flag-en.svg', code: 'EN' },
  ro: { flag: 'assets/images/flag-ro.svg', code: 'RO' }
};

function applyLanguage(lang) {
  window.currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  /* Text nodes */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = window.t(el.dataset.i18n);
    if (val) el.textContent = val;
  });

  /* HTML nodes */
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = window.t(el.dataset.i18nHtml);
    if (val) el.innerHTML = val;
  });

  /* Page title */
  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl) document.title = window.t(titleEl.dataset.i18n);

  /* Toggle button */
  const flagEl = document.getElementById('lang-flag');
  const codeEl = document.getElementById('lang-code');
  if (flagEl) flagEl.src = LANG_META[lang].flag;
  if (codeEl) codeEl.textContent = LANG_META[lang].code;

  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(window.currentLang);

  const btn = document.getElementById('lang-btn');
  if (btn) {
    btn.addEventListener('click', () => {
      applyLanguage(window.currentLang === 'en' ? 'ro' : 'en');
    });
  }
});
